// Data Types
var x;          // Undefined
console.log('x is of type, ', typeof(x));

x = 5;          // number
console.log('x is of type, ', typeof(x));

x = 'String';   // String
console.log('x is of type, ', typeof(x));

x = true;       // Boolean
console.log('x is of type, ', typeof(x));

x = [];         // Array
console.log('x is of type, ', typeof(x));

x = null;       // Null! Different from undefined
console.log('x is of type, ', typeof(x));

x = {};         // Object
console.log('x is of type, ', typeof(x));

